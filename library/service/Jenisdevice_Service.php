	public function insertdata(array $data){
		$registry = Zend_Registry::getInstance();
		$db = $registry->get('db');
		try {
	    $db->beginTransaction();
		
		$insdata = array("id_jenis" => $data['id_jenis'],
						 "kode_ket" => $data['kode_ket'],
						 "keterangan" => $data['keterangan'],
						 "seq" => $data['seq'],
						 "status" => $data['status']);
					
		$db->insert('jenisdevice',$insdata);
		$db->commit();
	    return 'sukses';
	   } catch (Exception $e) {
         $db->rollBack();
         echo $e->getMessage().'<br>';
	     return 'gagal';
	   }
	}
	
	public function editdata(array $data){
		$registry = Zend_Registry::getInstance();
		$db = $registry->get('db');
		try {
	    $db->beginTransaction();
		
		$id_jenis = $data['id_jenis'];
		
	    $insdata = array("kode_ket" => $data['kode_ket'],
						 "keterangan" => $data['keterangan'],
						 "status" => $data['status']);
						 
		$where = "id_jenis = '".$id_jenis."'";
					
		$db->update('jenisdevice',$insdata,$where);
		$db->commit();
	    return 'sukses';
	   } catch (Exception $e) {
         $db->rollBack();
         echo $e->getMessage().'<br>';
	     return 'gagal';
	   }
	}
	
