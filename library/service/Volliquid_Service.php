	public function insertdata(array $data){
		$registry = Zend_Registry::getInstance();
		$db = $registry->get('db');
		try {
	    $db->beginTransaction();
		
		$insdata = array("id_volume" => $data['id_volume'],
						 "volume" => $data['volume'],
						 "seq" => $data['seq'],
						 "status" => $data['status']);
					
		$db->insert('volliquid',$insdata);
		$db->commit();
	    return 'sukses';
	   } catch (Exception $e) {
         $db->rollBack();
         echo $e->getMessage().'<br>';
	     return 'gagal';
	   }
	}
	
	public function editdata(array $data){
		$registry = Zend_Registry::getInstance();
		$db = $registry->get('db');
		try {
	    $db->beginTransaction();
		
		$id_volume = $data['id_volume'];
		
	    $insdata = array("volume" => $data['volume'],
						 "status" => $data['status']);
						 
		$where = "id_volume = '".$id_volume."'";
					
		$db->update('volliquid',$insdata,$where);
		$db->commit();
	    return 'sukses';
	   } catch (Exception $e) {
         $db->rollBack();
         echo $e->getMessage().'<br>';
	     return 'gagal';
	   }
	}
	
	public function hapusData($dataInput){
